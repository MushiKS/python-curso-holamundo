print(1 > 2)  # Devuelve False porque 1 no es mayor que 2
print(1 < 2)  # Devuelve True porque 1 es menor que 2
print(1 <= 2)  # Devuelve True porque 1 es menor o igual a 2
print(1 >= 2)  # Devuelve False porque 1 no es mayor o igual a 2
print(2 >= 2)  # Devuelve True porque 2 es igual a 2 (cumple la condición de "mayor o igual")
print(2 <= 2)  # Devuelve True porque 2 es igual a 2 (cumple la condición de "menor o igual")
print(2 == 2)  # Devuelve True porque 2 es igual a 2
print(2 == 3)  # Devuelve False porque 2 no es igual a 3
print(2 == "2")  # Devuelve False porque 2 (entero) no es igual a "2" (cadena de texto)
print(2 != "2")  # Devuelve True porque 2 (entero) es diferente de "2" (cadena de texto)